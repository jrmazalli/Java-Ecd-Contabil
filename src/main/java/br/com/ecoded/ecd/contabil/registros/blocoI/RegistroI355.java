package br.com.ecoded.ecd.contabil.registros.blocoI;

public class RegistroI355 {

	private final String reg = "I355";
	private String cod_cta;
	private String cod_ccus;
	private String val_cta;
	private String ind_dc;
	private String val_cta_mf;
	private String ind_dc_mf;

	public String getCod_cta() {
		return cod_cta;
	}

	public void setCod_cta(String cod_cta) {
		this.cod_cta = cod_cta;
	}

	public String getCod_ccus() {
		return cod_ccus;
	}

	public void setCod_ccus(String cod_ccus) {
		this.cod_ccus = cod_ccus;
	}

	public String getVal_cta() {
		return val_cta;
	}

	public void setVal_cta(String val_cta) {
		this.val_cta = val_cta;
	}

	public String getInd_dc() {
		return ind_dc;
	}

	public void setInd_dc(String ind_dc) {
		this.ind_dc = ind_dc;
	}

	public String getVal_cta_mf() {
		return val_cta_mf;
	}

	public void setVal_cta_mf(String val_cta_mf) {
		this.val_cta_mf = val_cta_mf;
	}

	public String getInd_dc_mf() {
		return ind_dc_mf;
	}

	public void setInd_dc_mf(String ind_dc_mf) {
		this.ind_dc_mf = ind_dc_mf;
	}

	public String getReg() {
		return reg;
	}

}
